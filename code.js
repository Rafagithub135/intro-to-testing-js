// sayHello function

function sayHello(input) {
    return "Hello, " + input + "!";
}

function isFive(input) {
    return input % 5 === 0;
}

function isEven(input) {
    if (input % 2 === 0) {
        return true;
    } else if (input === false) {
        return false;
    } else if (input === '') {
        return false;
    }
}