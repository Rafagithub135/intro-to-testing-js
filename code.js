// sayHello function

function sayHello(input) {
    return "Hello, " + input + "!";
    if (input === 'Jane') {
        return "Hello, Jane!";
    }   else if (input === 'Alex') {
        return "Hello, Alex!";
    }   else if (input === 'Pat') {
        return "Hello, Pat!";
    }
}
